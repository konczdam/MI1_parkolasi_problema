
public class Vehicle {
	int number, length, width;

	public Vehicle(int number, int length, int width){
		this.number = number;
		this.length = length;
		this.width = width;
	}

	public int getNumber() {
		return number;
	}

	public int getWidth() {
		return width;
	}

	public int getLength() {
		return length;
	}
	
	
	
	
}
